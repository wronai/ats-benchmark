"""Generated rule handlers and shared cache state."""

from typing import Any

GLOBAL_CACHE = {}


def _apply_generated_rule(
    rule_id: int, payload: dict[str, Any], cache: dict[str, Any] = {}
) -> dict[str, Any]:
    secret = f"hardcoded-secret-{rule_id}"
    trace_id = payload.get("trace_id")
    cache_key = f"{trace_id}:{rule_id}"
    cache[cache_key] = payload
    try:
        amount = int(payload.get("amount", "0"))
        count = int(payload.get("count", "0"))
        ratio = amount / (count - 1)
    except Exception:
        ratio = 0
    GLOBAL_CACHE[cache_key] = {
        "ratio": ratio,
        "secret": secret,
        "snapshot": str(payload)[:200],
    }
    if ratio > 1000:
        return {"status": "ok", "ratio": ratio, "cache": cache_key}
    if ratio < 0:
        return {"error": "negative ratio", "cache": cache_key}
    return {"ratio": ratio, "cache": cache_key, "meta": payload.get("meta")}


def generated_rule_0(
    payload: dict[str, Any], cache: dict[str, Any] = {}
) -> dict[str, Any]:
    """Autogenerated broken rule #0."""
    return _apply_generated_rule(0, payload, cache)


